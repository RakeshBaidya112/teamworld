        var timeoutFunc;

        function SGAutoFilter() {
            clearTimeout(timeoutFunc);

            $('.glossary-item-description').hide();
            var input, filter, table, searchItem;
            input = $("#searchInput");
            filter = input.val().toUpperCase();
            table = $("#searchTable");
            searchItem = $("#searchTable .glossary-item");
            $('.shipping-glossary-arrow').hide();
            searchItem.each(function () {
                if ($(this)) {
                    $(this).removeClass('current');
                    if ($(this).find('span').html().toUpperCase().indexOf(filter) > -1) {
                        $(this).show();
                    } else {
                        $(this).hide();
                    }
                }
            });
            timeoutFunc = setTimeout(setDescriptionPosition, 1);
        }

        function setDescriptionPosition() {
            $(".glossary-item-description[_item_key]").each(function () {
                var item_key = $(this).attr("_item_key");
                var glossary_item = $(".glossary-item[_item_key='" + item_key + "']");
                $(this).css({ 'top': glossary_item.position().top + glossary_item.outerHeight(true) });
            })
        }

        $(document).ready(function () {
            $(".glossary-item-description").hide();
            setDescriptionPosition();

            //   Hide the border by commenting out the variable below
            var $on = 'section';
            $($on).css({
                'background': 'none',
                'border': 'none',
                'box-shadow': 'none'
            });

            $(".glossary-item").click(function () {
                var item_key = $(this).attr("_item_key");

                if ($(this).hasClass("current")) {
                    $(".glossary-item.current").removeClass("current");
                    $(".glossary-item-description[_item_key='" + item_key + "']").fadeOut();
                    $(".shipping-glossary-arrow").fadeOut();
                }
                else {
                    $(".glossary-item.current").removeClass("current");
                    $(this).addClass("current");


                    $(".glossary-item-description:visible").hide();

                    $(".glossary-item-description[_item_key='" + item_key + "']").fadeIn();

                    setArrowPosition($(this));
                }

            })

            $(".glossary-item-description>span.icon-cancel").click(function () {
                $(".glossary-item.current").removeClass("current");
                $(".shipping-glossary-arrow").fadeOut();
                $(".glossary-item-description:visible").fadeOut();

            })

            function setArrowPosition(activeCell) {

                $(".shipping-glossary-arrow").hide();

                var cellPos = $(activeCell).position();
                //bug fixing here for IE, should use the detail row top, cause the cell outHeight() property is different in IE

                var itemWidth = $(activeCell).find("span").width();
                if (isNaN(itemWidth)) {
                    itemWidth = $(activeCell).width();
                }

                $(".shipping-glossary-arrow").css({ 'left': cellPos.left + itemWidth / 2, 'top': $(activeCell).position().top + $(activeCell).outerHeight(true) - 11 });


                $(".shipping-glossary-arrow").fadeIn();
            }

            $(window).resize(function () {
                setDescriptionPosition();
                $(".glossary-item.current").removeClass("current");
                $(".glossary-item-description").hide();
                $(".shipping-glossary-arrow").hide();

            })
        });
